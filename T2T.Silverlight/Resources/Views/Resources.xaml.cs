namespace T2T.Views
{
    public partial class Resources : Page
    {
        public Resources()
        {
            InitializeComponent();
        }

        // Executes when the user navigates to this page.
        protected override void OnNavigatedTo(NavigationEventArgs e)
        {
        }

        private void resourcesFrame_Navigated(object sender, NavigationEventArgs e)
        {
            foreach (UIElement child in ResourcesLinksStackPanel.Children)
            {
                HyperlinkButton hb = child as HyperlinkButton;
                if (hb != null && hb.NavigateUri != null)
                {
                    if (hb.NavigateUri.ToString().Equals(e.Uri.ToString()))
                    {
                        VisualStateManager.GoToState(hb, "ActiveLink", true);
                    }
                    else
                    {
                        VisualStateManager.GoToState(hb, "InactiveLink", true);
                    }
                }
            }
        }

        private void resourcesFrame_NavigationFailed(object sender, NavigationFailedEventArgs e)
        {

        }

    }
}
